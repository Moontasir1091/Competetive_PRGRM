#include <bits/stdc++.h>
using namespace std;

int main() {
    int n;
    cin >> n;
    vector<int> vec(n);

    for (int i = 0; i < n; ++i) {
        cin >> vec[i];
    }

   
    map<int, int> freq;
    for (int num : vec) {
        freq[num]++;
    }

   
    int max_Freq = 0;
    for (auto ele : freq) {
        max_Freq = max(max_Freq, ele.second);
    }

    if (max_Freq > (n + 1) / 2) {
        cout << "NO" << endl;
        return 0;
    }

    cout << "YES" << endl;
}

